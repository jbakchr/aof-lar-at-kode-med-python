"""
Examples of for loops
"""

# EXAMPLE 1 - RANGE LOOPS

# range with only "stop" value
for i in range(11):
  print(f"Num is: {i}")

# range with "start" and "stop" value
for i in range(1, 11):
  print(f"Num is: {i}")

# range with "start", "stop" and "step" value
for i in range(1, 11, 2):
  print(f"Num is: {i}")


