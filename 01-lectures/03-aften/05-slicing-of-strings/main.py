"""
Examples of slicing a string
"""

# Example 1 - String slicing with start and stop value
ex1 = "Hello, World!"
print(ex1[0:5])


# Example 2 - String slicing only with stop value
ex2 = "Awesome sauce"
print(ex2[:7])


# Example 3 - String slicing only with start value
ex3 = "Slice me!"
print(ex3[6:])


# Example 4 - String slicing with negative start and stop values
ex4 = "We are the knights who say Ni ..."
print(ex4[-6:-1])