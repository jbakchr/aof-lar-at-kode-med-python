"""
Examples of booleans in Python
"""

# Example 1 - Assign boolean values to variables
is_true = True
is_false = False


# Example 2 - Creating a boolean variable by use of the "bool()" constructor function
also_true = bool(True)


# Example 3 - Evaluating an expression to a boolean value
this_is_true = 10 > 9
this_is_not_true = 10 < 9


# Example 4 - Evaluating boolean expression in an "if" statement
if "J" in "Jonas":
  print("A 'J' exists in the name 'Jonas'")
