"""
Examples of using escape characters

Tip: list of escape characters can be found here: https://docs.python.org/3/reference/lexical_analysis.html#string-and-bytes-literals 
"""

# Example 1 - Escaping double quotes within double quotes
print("Hi, my nick name is \"John Doe\"")


# Example 2 - Escaping backslahes within a string
print("C:\\Users\\jonasphillipson\\Desktop")


# Example 3 - Escape character using new line
print("I'm on the first line\nAnd I'm on the second line")


# Example 4 - Escape character using tab
print("There's a\ttab between us ..")