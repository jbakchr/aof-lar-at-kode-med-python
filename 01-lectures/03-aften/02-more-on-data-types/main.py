"""
Various examples of the constructor functions from different data types
"""

# strings
name = str("Jonas")

# integer
number = int(10)

# float - with type casting 
bank_account = float(number)
print(bank_account)

# boolean
you_are_awesome = bool(True)

# list
lst = list(["apple", "banana", "apple"])
print(lst)

# set - with 
groceries = set(lst)
print(groceries)