"""
Various examples of using the "type" function
"""

# print type of a string
name = "Jonas"
print(type(name))

# print type of an integer
age = 41
print(type(age))

# print type of a list
groceries = list(["apple", "banana", "beer"])
print(type(groceries))


# check if the variable "name" is a string
if name == str:
  print("Whoooa .. this variable is indeed a string!")