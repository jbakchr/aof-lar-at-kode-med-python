"""
Examples of comparison operators
"""

# Example 1 - equals operator
ex1 = 10 == 10  # True
ex2 = 10 == 20  # False


# Example 2 - not equals operator
ex3 = 10 != 10  # False
ex4 = 10 != 20  # True


# Example 3 - greater than operator
ex5 = 10 > 10  # False
ex6 = 20 > 10  # True


# Example - less than operator
ex7 = 10 < 10  # False
ex8 = 10 < 20  # True

