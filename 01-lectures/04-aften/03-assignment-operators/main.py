"""
Examples of assignment operators
"""

num1 = 20
num2 = 3


# Example 1 - Addition - num1 becomes 23
num1 += num2
print(num1)


# Example 2 - Subtraction - num1 becomes 20 again
num1 -= num2
print(num1)


# Example 3 - Multiplication - num1 now becomes 60
num1 *= num2
print(num1)


# Example 4 - Division - num1 becomes 20 again
num1 /= num2
print(num1)